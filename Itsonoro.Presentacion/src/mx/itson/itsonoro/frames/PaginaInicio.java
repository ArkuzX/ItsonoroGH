    private void txtEmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtEmailFocusLost
        String correov = txtEmail.getText();

        List<Usuario> Datos = new ArrayList<>();

        Datos = listaUsuarios.stream().filter(item -> item.getCorreoelectronico().equalsIgnoreCase(correov))
                .collect(Collectors.toList());

        boolean registrado = false;
        for (Usuario user : Datos) {
            if (user.getCorreoelectronico().equals(correov)) {
                registrado = true;
            } else {
                registrado = false;
            }
        }
        if (registrado == true) {
            lblEmail.setText("E-mail : (correo ya existe)");
            lblEmail.setForeground(Color.red);
            correoregistrado = true;
        } else if (registrado == false) {
            lblEmail.setText("E-mail :");
            lblEmail.setForeground(Color.black);
            correoregistrado = false;
        }
    }//GEN-LAST:event_txtEmailFocusLost

