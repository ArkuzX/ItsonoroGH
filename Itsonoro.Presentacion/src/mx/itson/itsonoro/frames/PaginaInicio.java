    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed

        try {

            String email = txtEmail.getText();
            String telefono = txtTelefono.getText();
            Pattern pat = Pattern.compile("^[\\w-]+(\\.[\\w-]+)*@[A-Za-z0-9]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
            Matcher mat = pat.matcher(email);
            String contraseña = new String(pswcontraseña.getPassword());
            String confirmarcontraseña = new String(pswconfirmarcontraseña.getPassword());

            if (txtNombre.getText().equals("")
                    || txtEmail.getText().equals("")
                    || txtTelefono.getText().equals("")
                    || txtFechaNacimiento.getText().equals("")
                    || txtInstitucion.getText().equalsIgnoreCase("")
                    || CbxTipoDeUsuario.getSelectedItem().equals("SELECCIONAR")) {

                JOptionPane.showMessageDialog(null, "Favor De Llenar toda la informacion", "Error", JOptionPane.ERROR_MESSAGE);

                if (!mat.find()) {

                    JOptionPane.showMessageDialog(null, "Favor De Verificar Correo Electronico", "Error", JOptionPane.ERROR_MESSAGE);
                    lblEmail.setForeground(Color.red);
                    txtEmail.setText("");

                    if (!telefono.matches("[0-9]*")) {

                        JOptionPane.showMessageDialog(null, "Favor De Verificar Telefono", "Error", JOptionPane.ERROR_MESSAGE);
                        lblTelefono.setForeground(Color.red);
                        txtTelefono.setText("");

                        if (correoregistrado == true) {

                            JOptionPane.showMessageDialog(null, "Correo ya Registrado", "Error", JOptionPane.ERROR_MESSAGE);
                            lblEmail.setForeground(Color.red);

                            if (!confirmarcontraseña.equals(contraseña)) {

                                JOptionPane.showMessageDialog(null, "Las Contraseñas deben ser iguales", "Error", JOptionPane.ERROR_MESSAGE);
                                lblContraseña.setForeground(Color.red);
                                lblConfirmarContraseña.setForeground(Color.red);

                            }

                        }

                    }

                }

            } else {

                lblContraseña.setForeground(Color.black);
                lblConfirmarContraseña.setForeground(Color.black);
                lblEmail.setForeground(Color.black);
                lblTelefono.setForeground(Color.black);
                lblEmail.setText("E-mail :");

                Usuario usuario = new Usuario();
                usuario.setNombre(txtNombre.getText());
                usuario.setCorreoelectronico(txtEmail.getText());
                usuario.setCumpleaños(txtFechaNacimiento.getText());
                usuario.setInstitucion(txtInstitucion.getText());
                usuario.setTipoUsuario(TipoUsuario.valueOf(CbxTipoDeUsuario.getSelectedItem().toString()));
                usuario.setId(listaUsuarios.size());

                String contra = new String(pswcontraseña.getPassword());
                usuario.setContraseña(contra);

                operacion.Agregar(listaUsuarios, usuario);

                try {

                    operacion.ToJson(listaUsuarios, datos);

                } catch (Exception e) {

                    JOptionPane.showMessageDialog(null, "no sirve");

                }

                operacion.CargarTxt(datos, listaUsuarios);

                txtNombre.setText("");
                txtEmail.setText("");
                txtFechaNacimiento.setText("");
                txtInstitucion.setText("");
                txtTelefono.setText("");
                CbxTipoDeUsuario.setSelectedIndex(0);
                pswconfirmarcontraseña.setText("");
                pswcontraseña.setText("");

                JOptionPane.showMessageDialog(null, "El Usuario: " + usuario.getNombre() + " ha sido agregado", "Exito", JOptionPane.INFORMATION_MESSAGE);

            }

        } catch (Exception e) {
        }

    }//GEN-LAST:event_BtnRegistrarActionPerformed

